import java.util.*;
public class sum {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;//Sum
        int mul = a * b;//Multiplication
        System.out.println(sum);
        System.out.println(mul);
        sc.close();
    }
}
